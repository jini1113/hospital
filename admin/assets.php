function calculateWarrantyYears($months)
{
    $years = floor($months / 12);
    $remainingMonths = $months % 12;

    if ($years > 0 && $remainingMonths > 0) {
        return $years . " year(s) " . $remainingMonths . " month(s)";
    } elseif ($years > 0) {
        return $years . " year(s)";
    } else {
        return $months . " month(s)";
    }
}

